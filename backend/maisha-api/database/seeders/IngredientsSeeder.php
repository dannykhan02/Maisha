class IngredientsSeeder extends Seeder
{
    public function run(): void
    {
        $ingredients = array_merge(
            $this->proteins(),
            $this->legumes(),
            $this->staples(),
            $this->vegetables(),
            $this->leafyGreens(),
            $this->fruits(),
            $this->dairy(),
            $this->fatsAndOils(),
            $this->spicesAndHerbs(),
            $this->nutsAndSeeds(),
            $this->traditionalFoods()
        );

        $now = now();

        foreach ($ingredients as $ingredient) {
            DB::table('ingredients')->insertOrIgnore(array_merge($ingredient, [
                'created_at' => $now,
                'updated_at' => $now,
            ]));
        }

        $this->command->info('✓ Ingredients seeded: ' . count($ingredients) . ' items across ' . $this->categoryCount($ingredients) . ' categories');
    }

    private function categoryCount(array $ingredients): int
    {
        return count(array_unique(array_column($ingredients, 'category')));
    }

